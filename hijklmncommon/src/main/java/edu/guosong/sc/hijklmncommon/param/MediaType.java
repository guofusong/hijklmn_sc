package edu.guosong.sc.hijklmncommon.param;

public enum MediaType {

	image(1, "image"), // 图片
	sound(2, "sound"), // 声音
	video(3, "video"); // 视频

	private Integer code;

	private String type;

	private MediaType(int code, String type) {
		this.code = code;
		this.type = type;
	}

	public int code() {
		return this.code;
	}

	public String type() {
		return this.type;
	}

}
